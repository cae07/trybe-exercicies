a = 20
b = 10

print(a + b) # soma => saida 30
print(a - b) # subtação => saida 10
print(a * b) # multiplicação => saida 200
print(a / b) # divisão => saida 2.0
print(a // b) # quociente (arredonda pra baixo) => saida 2
print(a ** b) # exponenciação => saida 10240000000000
print(a ** (1/2)) # raiz quadrada => saida 4.47213595499958
